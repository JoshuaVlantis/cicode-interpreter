export function registerAiContextCommand(
  context: vscode.ExtensionContext,
): vscode.Disposable {
  return vscode.commands.registerCommand(
    "cicode.addAIContext",
    async () => {
      const folder = vscode.workspace.workspaceFolders?.[0];
      if (!folder) {
        vscode.window.showErrorMessage(
          "Cicode: No workspace folder open. Please open a folder first.",
        );
        return;
      }

      const root = folder.uri.fsPath;

      // --- CLAUDE.md ---
      const claudePath = path.join(root, "CLAUDE.md");
      fs.writeFileSync(claudePath, CONTENT, "utf8");

      // --- .github/copilot-instructions.md ---
      const githubDir = path.join(root, ".github");
      if (!fs.existsSync(githubDir)) {
        fs.mkdirSync(githubDir, { recursive: true });
      }
      const copilotPath = path.join(githubDir, "copilot-instructions.md");
      fs.writeFileSync(copilotPath, CONTENT, "utf8");

      const choice = await vscode.window.showInformationMessage(
        "Cicode: Created CLAUDE.md and .github/copilot-instructions.md",
        "Open CLAUDE.md",
      );
      if (choice === "Open CLAUDE.md") {
        const doc = await vscode.workspace.openTextDocument(claudePath);
        await vscode.window.showTextDocument(doc);
      }
    },
  );
}
